//shift()
const Students = ["Cali", "Maxamad", "Warfaa"];
console.log(Students);
console.log(Students.shift()); // cali
console.log(Students);
console.log(Students.shift()); // Maxamad
console.log(Students);
console.log(Students.shift()); // Warfaa
console.log(Students); // []
// pop()
const Colors = ["Red", "Green", "blue"];
console.log(Colors);
console.log(Colors.pop()); // Blue
console.log(Colors);
console.log(Colors.pop()); // Green
console.log(Colors);
console.log(Colors.pop()); // red
console.log(Colors);
